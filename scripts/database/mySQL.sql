DROP DATABASE IF EXISTS smart;
CREATE DATABASE smart;
use smart;
CREATE TABLE gruppe (
id int(10) NOT NULL,
name varchar(50),
manager varchar(50),
description varchar(100),
PRIMARY KEY (id)
);
CREATE TABLE ou (
id int(10) NOT NULL,
name varchar(50),
description varchar(100),
PRIMARY KEY (id)
);
CREATE TABLE benutzer (
id int(10) NOT NULL,
ou int(10),
login varchar(50),
password varchar(50),
description varchar(50), #Position/Aufgabe
vorname varchar(50),
nachname varchar(50),
email varchar(50),
ophone varchar(50),
mphone varchar(50),
company varchar(50),
abteilung varchar(50),
office varchar(50),
PRIMARY KEY (id),
FOREIGN KEY (ou) REFERENCES ou(id)
);
CREATE TABLE rechner (
id int(10) NOT NULL,
ou int(10),
name varchar(50), #pc_xyz
description varchar(100), #bla bla
displayname varchar(50), #pc_person
manage varchar(50), #admin
os varchar(50), #Windows 8
PRIMARY KEY (id),
FOREIGN KEY (ou) REFERENCES ou(id)
);
CREATE TABLE benutzer_gruppe (
benutzer int(10),
gruppe int(10),
PRIMARY KEY (benutzer,gruppe),
FOREIGN KEY (benutzer) REFERENCES benutzer(id),
FOREIGN KEY (gruppe) REFERENCES gruppe(id)
);
CREATE TABLE rechner_gruppe (
rechner int(10),
gruppe int(10),
PRIMARY KEY (rechner,gruppe),
FOREIGN KEY (rechner) REFERENCES rechner(id),
FOREIGN KEY (gruppe) REFERENCES gruppe(id)
);
